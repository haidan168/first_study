<template>
  <swiper>
    <swiper-item v-for="(item,index) in banners"
                 :key="index">
      <a :href="item.link">
        <img :src="item.image"
             alt=""
             @load="imageLoad">
      </a>
    </swiper-item>
  </swiper>
</template>

<script>
import { Swiper, SwiperItem } from 'components/common/swiper'
export default {
  name: 'HomeSwiper',
  props: {
    banners: {
      type: Array,
      default () {
        return []
      }
    }
  },
  data () {
    return {
      isLoad: false
    }
  },
  components: {
    Swiper,
    SwiperItem
  },
  methods: {
    // 监听轮播图加载是否完成
    imageLoad () {
      // 判断是否将事件发出
      if (!this.isLoad) {
        this.$emit('swiperImageLoad');
        this.isLoad = true;
      }
    }
  }
}
</script>

